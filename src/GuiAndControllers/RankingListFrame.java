    /**
     * show the habit at table base on the max streak day
     */
    public void quaryRank() {
        String name = ModeTranslators.user.getUsername();
        // get the order list
        list = habits.getRanking();
        if (list == null) {
            JOptionPane.showMessageDialog(null, "system errorÔºÅ");
            return;
        }
        //get data of habits
        Object[][] data = DateListHabits.listToArray4Rank(list);
        DefaultTableModel tableModel = new DefaultTableModel(data, this.columnTitle);
        // build new table
        this.jtable_Ranking.setModel(tableModel);
        this.jtable_Ranking.setEnabled(false);
    }
    
    

